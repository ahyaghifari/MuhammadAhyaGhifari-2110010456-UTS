    // method untuk mengenerate id reseo secara otomatis
   public String makeIdResepMasakan(){
       ResepMasakan lastResep = FrameAplikasiResepMasakan.resepResepMasakan.get(FrameAplikasiResepMasakan.resepResepMasakan.size() - 1); // mengambil elemen resep pada index terakhir dari array list resep
       String lastIndex = lastResep.getIdResepMasakan(); // mengambil id dari element resep yang terakhir
       String lastId  = lastIndex.substring(3); // mengambil angka pertama sampai seterusnya dari resep
       int id = Integer.parseInt(lastId) + 1;  // menambahkan 1 angka pada id terakhir sehingga increment
       return "RSP" + String.valueOf(id); // mengembalikan id yang sudah ditambahkan
   }
    
