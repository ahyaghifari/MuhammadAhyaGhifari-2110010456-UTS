    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        if(status == 0){ // ketika sedang menambah
            resetForm();
        }else{ // ketika sedang mengubah
            int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin ingin mereset resep yang sudah ada", "Reset", JOptionPane.WARNING_MESSAGE);
            if(konfirmasi == 0){
                resetForm();
            }
        }
    }//GEN-LAST:event_btnResetActionPerformed

