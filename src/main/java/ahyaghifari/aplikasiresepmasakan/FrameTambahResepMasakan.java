    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed

        // mengambil nilai dari komponen komponen pada desain
        String idResep = tfIdResep.getText();
        String namaMasakan = tfNamaMasakan.getText();
        String lamaMemasak = tfLamaMemasak.getText();
        String bahanBahan = taBahanBahan.getText();
        String caraMemasak = taCaraMemasak.getText();
        
        // validasi form apakah sudah diisi atau belum
        if(namaMasakan.equals("") || lamaMemasak.equals("") || bahanBahan.equals("") || caraMemasak.equals("")){
        // jika masih ada yang belum diisi
        
            JOptionPane.showMessageDialog(this, "Input masih ada yang belum diisi", "INPUT", JOptionPane.ERROR_MESSAGE);
                
        // jika semua sudah terisi    
        }else{
            
        
        // TAMBAH : menambahkan resep makanan ketilka status 0 atau sedang tambah
        if(status == 0){ 
            FrameAplikasiResepMasakan.resepResepMasakan.add(new ResepMasakan(idResep, namaMasakan, lamaMemasak, bahanBahan, caraMemasak));
            JOptionPane.showMessageDialog(this, "Resep " + namaMasakan + " berhasil ditambahkan", "Resep Baru", JOptionPane.INFORMATION_MESSAGE);
        
        // EDIT : mengedit resep masakan ketika status sedang 1 atau sedang mengedit
        }else{
            
            int listIndex = 0; // inisialisasi index
            
            // // melakukan perulangan untuk mencari index yang cocok dari id pada array list dengan id text field id
            for(ResepMasakan rm: FrameAplikasiResepMasakan.resepResepMasakan){
                if(rm.getIdResepMasakan().equals(idResep)){
                    listIndex = FrameAplikasiResepMasakan.resepResepMasakan.indexOf(rm); // mengambil index
                    break;
                }
            }
            
            // membuat objek resep baru
            ResepMasakan updateResep = new ResepMasakan(tfIdResep.getText(), namaMasakan, lamaMemasak, bahanBahan, caraMemasak);
            
            // menimpa objek resep lama dengan objek resep yang baru sesuai index yang didapat
            FrameAplikasiResepMasakan.resepResepMasakan.set(listIndex, updateResep);
            
            // pesan informasi
            JOptionPane.showMessageDialog(this, "Resep " + namaMasakan + " berhasil diubah", "Update Resep", JOptionPane.INFORMATION_MESSAGE);
            
            // memanggil dan menampilkan frame detail dengan data yang sudah diubah
            FrameDetailResepMasakan detailFrame = new FrameDetailResepMasakan(updateResep);
            detailFrame.setVisible(true);
            
        }
        dispose();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

